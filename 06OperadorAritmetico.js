// Empezaremos por el operador +
let a = 3, b = 2; // declaramos 2 variables con valores
let z = a + b; // declaramos z y hacemos la suma de a + b
// mostramos el resultado
console.log("Resultado de la resta: " + z)

// Ahora para la resta
z = a - b; // restamos a - b
console.log("Resultado de la resta: " + z)

// Multiplicación
z = a * b; // multiplicamos a * b
console.log("Resultado de la multiplicación: " + z)

// División
z = a / b; // Dividimos a / b
console.log("Resultado de la división: " + z)

// Residuo de la división / lo que sobra
z = a % b; // sobrante de la división a / b
console.log("Resultado del residuo: " + z)

// Operación exponentes
z = a ** b; // a elevado en b
console.log("Resultado de la elevación: " + z);

